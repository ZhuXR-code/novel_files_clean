export class ChineseConverter {
  /** 繁体转简体；空串或失败时原样返回。 */
  public static toSimplified(text: string): string {
    if (!text || text.length === 0) {
      return text;
    }
    let out: string = '';
    for (const ch of text) {
      out += TRAD_TO_SIMPLE[ch] ?? ch;
    }
    return out;
  }

  /** 生成拼音串（全拼空格分隔|首字母连写）；空串返回空串，失败返回空串。 */
  public static toPinyin(text: string): string {
    if (!text || text.length === 0) {
      return '';
    }
    try {
      const fullArr: string[] = pinyin(text, { toneType: 'none', type: 'array' }) as string[];
      const firstArr: string[] = pinyin(text, { pattern: 'first', toneType: 'none', type: 'array' }) as string[];
      return `${fullArr.join(' ')}|${firstArr.join('')}`;
    } catch (e) {
      return '';
    }
  }
}
